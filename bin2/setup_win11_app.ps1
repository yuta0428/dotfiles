# How to use
## PS:> powershell -ExecutionPolicy Unrestricted ./setup_win11_app.ps1

# Application

## Git
winget install Git.Git

## Visual Studio Code
## 右クリックでのコンテキストメニューを追加するなら、インストーラー経由で入れ直す
winget install Microsoft.VisualStudioCode

## Slack
winget install SlackTechnologies.Slack

## Chrome
winget install Google.Chrome

## Sourcetree
winget install Atlassian.Sourcetree

## Bandicam
winget install BandicamCompany.Bandicam

## 1Password
winget install AgileBits.1Password

## Adobe Acrobat Reader
winget install Adobe.Acrobat.Reader.64-bit

## Ruby2.6
winget install RubyInstallerTeam.RubyWithDevKit.2.6

## Zoom
winget install Zoom.Zoom