# Application

## Git
winget install Git.Git

## Visual Studio Code
## 右クリックでのコンテキストメニューを追加するなら、インストーラー経由で入れ直す
winget install Microsoft.VisualStudioCode

## Slack
winget install SlackTechnologies.Slack

## Chrome
winget install Google.Chrome

